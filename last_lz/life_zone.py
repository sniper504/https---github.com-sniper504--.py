# написать код для выяснения среднего радиуса обитаемой зоны вокруг звезды используя формулу
#===========================================================================================

def life_zone(a):
    dau = float(a)/ 3.86
    return dau

def main():
    print("введите болометрический показатель звезды")
    user_input_star = input()
    result = life_zone(user_input_star)
    print("средний радиус обитаемой зоны вокруг звезды равен", result)

if __name__ == '__main__':
    main()